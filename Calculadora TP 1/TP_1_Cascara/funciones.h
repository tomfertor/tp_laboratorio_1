#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED
/** \brief Realiza la SUMA de dos numeros ingresados por el usuario.
 * \param Primer Numero flotante ingresado por el usuario.
 * \param Segundo Numero flotante ingresado por el usuario.
 * \return El resultado de la suma de los numeros.
 */
float suma(float , float );
/** \brief Realiza la RESTA de dos numeros ingresados por el usuario.
 * \param Primer Numero flotante ingresado por el usuario.
 * \param Segundo Numero flotante ingresado por el usuario.
 * \return El resultado de la resta de los numeros.
 */
float resta(float , float );
/** \brief Realiza la DIVISION de dos numeros ingresados por el usuario.
 * \param Primer Numero flotante ingresado por el usuario.
 * \param Segundo Numero flotante ingresado por el usuario.
 * \return El resultado de la division de los numeros.
 */
float dividir(float , float );
/** \brief Realiza la MULTIPLICACION de dos numeros ingresados por el usuario.
 * \param Primer Numero flotante ingresado por el usuario.
 * \param Segundo Numero flotante ingresado por el usuario.
 * \return El resultado de la multiplicacion de los numeros.
 */
float multiplicar(float , float );
/** \brief Realiza el FACTORIAL del primer numero ingresado por el usuario.
 * \param Primer Numero ingresado por el usuario.
 * \return El resultado del calculo del factorial del primer numero ingresado por el usuario.
 */
int factorial(float );
/** \brief Informa que en el caso de que el segundo numero ingresado por el usuario sea 0, la division no se puede realizar.
 * \param Segundo Numero flotante ingresado por el usuario.
 * \return Nada.
 */
void dividirPorCero(int );
#endif // FUNCIONES_H_INCLUDED
